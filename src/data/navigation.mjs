export function getActiveTab(pathname) {
  const clean = pathname.replace(/^\//, '').replace(/\/$/, '');
  for (const tab of tabs) {
    if (tab.pages.some(p => clean === p || clean.startsWith(p + '/'))) {
      return tab;
    }
  }
  return tabs[0];
}
